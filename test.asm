.nolist
.include "m328Pdef.inc"
.list

.equ LED_0 = PB1
.equ LED_1 = PB2
.equ LED_2 = PB3
.equ LED_3 = PB4

.ORG 0x000
INICIO:
	LDI R16,0xFF
	OUT DDRB,R16
PRINCIPAL:
	SBI PORTB, LED_0
	SBI PORTB, LED_1
	SBI PORTB, LED_2
	SBI PORTB, LED_3
	RCALL ATRASO
	CBI PORTB, LED_0
	CBI PORTB, LED_1
	CBI PORTB, LED_2
	CBI PORTB, LED_3
	RCALL ATRASO
	RJMP PRINCIPAL
ATRASO:

volta:
	DEC R17
	BRNE volta
	DEC R18
	BRNE volta
	DEC R19
	BRNE volta
	RET
